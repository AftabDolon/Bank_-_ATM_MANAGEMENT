    private void SignupPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SignupPanel1MouseClicked
        Box box = Box.createHorizontalBox();

        JLabel jl = new JLabel("Password: ");
        box.add(jl);

        JPasswordField jpf = new JPasswordField(16);
        box.add(jpf);

        int button = JOptionPane.showConfirmDialog(null, box, "Enter your password", JOptionPane.OK_CANCEL_OPTION);
        String pass = "admin";

        if (button == JOptionPane.OK_OPTION) {
            char[] input = jpf.getPassword();
            String enteredPass = String.valueOf(input);
            if (enteredPass.equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill the Password Field", "Login Error!", JOptionPane.ERROR_MESSAGE);
            } else if (enteredPass.equals(pass)) {
                myDriver();
                int id;
                try {
                    String n="admin";
                    String cdate="2022-05-01";
                     id=1000;
                    String query = "insert into userinfo values(" + id + ",'" + n + "','" + cdate + "','" + Date + " " + time + "','" + "N/A')";
                    PreparedStatement pst5 = con.prepareStatement(query);
                    pst5.execute();
                    this.setVisible(false);
                    dispose();
                    new Report(id).setVisible(true);
                } catch (SQLException e) {
                    e.printStackTrace();
                }

            } else {
                JOptionPane.showMessageDialog(this, "Wrong Password !\nThis page only for Admin\nPlease contact him", "Login Error!", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_SignupPanel1MouseClicked

