    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        myDriver();

        try {
            if (uid.getText().equals("") || upass.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill the Username & Password Field", "Login Error!", JOptionPane.ERROR_MESSAGE);
                uid.requestFocus();
            } else {
                int user = Integer.parseInt(uid.getText());
                stm = con.createStatement();
                //select aes_decrypt(trainor_password, 'key') from trainor where trainor_id=1002;
                rs = stm.executeQuery("select id, first_name, last_name, password, date from employees where id=" + Integer.parseInt(uid.getText()) + "&& password='" + upass.getText() + "'");
                if (rs.next()) {
                    //System.out.println(rs);
                    //s = rs.getString("user_pass");
                    String n = rs.getString("First_Name") + " " + rs.getString("Last_Name");
                    String cdate = rs.getString("date");
                    String query = "insert into userinfo values(" + Integer.parseInt(uid.getText()) + ",'" + n + "','" + cdate + "','" + Date + " " + time + "','" + "N/A')";
                    PreparedStatement pst5 = con.prepareStatement(query);
                    pst5.execute();
                    this.setVisible(false);
                    dispose();
                    new MyDashboard(user).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(Login.this, "Sorry, Username or Password is Wrong", "Login Error!", JOptionPane.ERROR_MESSAGE);
                    uid.requestFocus();
                }
            }
        } catch (SQLException sqle) {
            System.out.println(sqle);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

